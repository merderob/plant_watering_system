#include "valve.h"

void Valve::init()
{
    relay_pin_ = Params::pump_relay_pin;
    pinMode(relay_pin_, OUTPUT);
    digitalWrite(relay_pin_, LOW);
}

void Valve::open()
{
    digitalWrite(relay_pin_, HIGH);
}

void Valve::close()
{
    digitalWrite(relay_pin_, LOW);
}
